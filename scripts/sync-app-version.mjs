import { readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const pkg = JSON.parse(readFileSync(join(root, "package.json"), "utf8"));
const version = pkg.version;
const builtAt = new Date().toISOString();

const output = `// Auto-generated by scripts/sync-app-version.mjs — do not edit.
export const APP_VERSION = ${JSON.stringify(version)};
export const APP_BUILT_AT = ${JSON.stringify(builtAt)};
`;

writeFileSync(join(root, "src/lib/app-version.generated.ts"), output, "utf8");

console.log(`[sync-app-version] ${version} @ ${builtAt}`);
